def tuples_to_dict(tuples_list):
    """
    Converts a list of tuples into a dictionary where strings are keys and integers are values.

    Args:
    tuples_list (list): The list of tuples to convert. Each tuple should contain a string and an integer.

    Returns:
    dict: A dictionary with strings as keys and integers as values.
    """
    return {string: integer for string, integer in tuples_list}


# Example usage:
example_tuples = [("apple", 5), ("banana", 3), ("cherry", 10)]
result = tuples_to_dict(example_tuples)
print(result)  # Output: {'apple': 5, 'banana': 3, 'cherry': 10}


